import { Schema, model } from 'mongoose'
import { IUser } from '../types'

const userSchema = new Schema<IUser>({
  name: {
    type: String,
    required: true,
    min: 6,
    max: 255
  },
  email: {
    type: String,
    required: true,
    min: 6,
    max: 255
  },
  password: {
    type: String,
    required: true,
    min: 6,
    max: 1024
  },
  date: {
    type: Date,
    default: Date.now
  }
})

export default model<IUser>('User', userSchema)
